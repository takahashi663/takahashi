package question07;

public class Question07_01 {

	public static void main(String[] args) {
		int num1 = 12;
		int num2 = 3;
		
		System.out.println("num1+num2は" + (num1 + num2) + "です。");
		System.out.println("num1-num2は" + (num1 - num2) + "です。");
		System.out.println("num1*num2は" + (num1 * num2) + "です。");
		System.out.println("num1/num2は" + (num1 / num2) + "です。");
		System.out.println("num1%num2は" + (num1 % num2) + "です。");
	}

}
